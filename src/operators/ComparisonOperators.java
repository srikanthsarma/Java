package operators;

public class ComparisonOperators {

	public static void main(String[] args) {
		int a = 5, b = 4;
		
//		Comparison Operations 
		System.out.println(a > b);
		System.out.println(a < b);
		System.out.println(a == b);
		System.out.println(a >= b);
		System.out.println(a <= b);
		System.out.println(a != b);
		
/*		Note: Comparison Operators always return a boolean value	*/		
		
	}

}
